//! Python test generator for html-to-markdown e2e tests.
//!
//! Generates `e2e/python/tests/test_{category}.py` and a `conftest.py`.

use crate::fixtures::Fixture;
use anyhow::Result;
use camino::Utf8Path;
use itertools::Itertools;
use std::collections::HashMap;
use std::fmt::Write as FmtWrite;

/// Generate Python e2e tests from fixtures into `output_dir/python/`.
pub fn generate(fixtures: &[Fixture], output_dir: &Utf8Path) -> Result<usize> {
    let python_dir = output_dir.join("python");
    let tests_dir = python_dir.join("tests");
    std::fs::create_dir_all(&tests_dir)?;

    // Create __init__.py files to make directories proper Python packages.
    std::fs::write(python_dir.join("__init__.py"), "")?;
    std::fs::write(tests_dir.join("__init__.py"), "")?;

    // Group fixtures by category.
    let by_category: HashMap<&str, Vec<&Fixture>> = fixtures
        .iter()
        .filter(|f| !f.is_skipped_for("python"))
        .into_group_map_by(|f| f.resolved_category());

    let mut total = 0;

    for (category, category_fixtures) in &by_category {
        let file_name = format!("test_{}.py", sanitize_module_name(category));
        let path = tests_dir.join(&file_name);
        let content = render_test_file(category, category_fixtures);
        std::fs::write(&path, content)?;
        total += category_fixtures.len();
    }

    // Generate conftest.py.
    let conftest = render_conftest();
    std::fs::write(tests_dir.join("conftest.py"), conftest)?;

    Ok(total)
}

fn render_conftest() -> String {
    r#""""Pytest configuration for html-to-markdown e2e tests."""
# Ensure the package is importable when running from the e2e/python directory.
# The html_to_markdown package is expected to be installed in the current environment.
"#
    .to_string()
}

fn render_test_file(category: &str, fixtures: &[&Fixture]) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "\"\"\"E2e tests for category: {category}.");
    let _ = writeln!(
        out,
        "Generated by html-to-markdown-e2e-generator — do not edit manually."
    );
    let _ = writeln!(out, "\"\"\"");
    let _ = writeln!(out, "# ruff: noqa: S101");
    let _ = writeln!(out, "import pytest");
    let has_options = fixtures.iter().any(|f| f.options.is_some());
    if has_options {
        let _ = writeln!(out, "from html_to_markdown import convert, ConversionOptions");
    } else {
        let _ = writeln!(out, "from html_to_markdown import convert");
    }
    let _ = writeln!(out);

    for fixture in fixtures {
        render_test_function(&mut out, fixture);
        let _ = writeln!(out);
    }

    out
}

fn render_test_function(out: &mut String, fixture: &Fixture) {
    let fn_name = sanitize_ident(&fixture.id);
    let description = &fixture.description;

    let _ = writeln!(out, "def test_{fn_name}() -> None:");
    // Ensure description ends with a period for D400 (docstring convention)
    let desc_with_period = if description.ends_with('.') {
        description.to_string()
    } else {
        format!("{}.", description)
    };
    let _ = writeln!(out, "    \"\"\"{desc_with_period}\"\"\"");

    // HTML input.
    let html = match &fixture.html {
        Some(html) => html.as_str(),
        None => {
            let _ = writeln!(out, "    pytest.skip(\"no inline html\")");
            return;
        }
    };

    let escaped_html = escape_python_string(html);
    let _ = writeln!(out, "    html = \"{escaped_html}\"");

    // Build ConversionOptions if specified.
    if let Some(opts) = &fixture.options {
        let entries: Vec<String> = opts
            .iter()
            .map(|(k, v)| {
                let snake_key = camel_to_snake(k);
                let py_val = json_value_to_python(v);
                format!("{snake_key}={py_val}")
            })
            .collect();
        let kwargs = entries.join(", ");
        let _ = writeln!(out, "    opts = ConversionOptions({kwargs})");
    }

    let convert_call = if fixture.options.is_some() {
        "convert(html, opts)"
    } else {
        "convert(html)"
    };

    // Conversion call + error handling.
    if fixture.assertions.expect_error == Some(true) {
        let match_param = fixture
            .assertions
            .error_contains
            .as_deref()
            .map(|s| format!(", match=r\"{}\"", escape_python_string(s)))
            .unwrap_or_default();
        let _ = writeln!(
            out,
            "    with pytest.raises(ValueError{match_param}) as exc_info:"
        );
        let _ = writeln!(out, "        {convert_call}");
        if let Some(contains) = &fixture.assertions.error_contains {
            let escaped = escape_python_string(contains);
            let _ = writeln!(out, "    assert \"{escaped}\" in str(exc_info.value)");
        }
        return;
    }

    let _ = writeln!(out, "    result = {convert_call}");

    let a = &fixture.assertions;

    // Check if we need the content variable
    let needs_content = a.content_equals.is_some()
        || a.content_not_empty == Some(true)
        || a.content_is_none == Some(true)
        || a.content_contains_all.is_some()
        || a.content_contains_any.is_some()
        || a.content_not_contains.is_some()
        || a.min_content_length.is_some()
        || a.max_content_length.is_some()
        || a.content_starts_with.is_some()
        || a.content_ends_with.is_some();

    if needs_content {
        let _ = writeln!(
            out,
            "    content = result if isinstance(result, str) else (result.get(\"content\") or \"\")"
        );
        let _ = writeln!(out);
    }

    // content_equals
    if let Some(expected) = &a.content_equals {
        let escaped = escape_python_string(expected);
        let _ = writeln!(out, "    assert content.strip() == \"{escaped}\"");
    }

    // content_not_empty
    if a.content_not_empty == Some(true) {
        let _ = writeln!(
            out,
            "    assert content.strip() != \"\", \"expected non-empty content\""
        );
    }

    // content_is_none
    if a.content_is_none == Some(true) {
        let _ = writeln!(
            out,
            "    assert content.strip() == \"\", \"expected empty/None content\""
        );
    }

    // content_contains_all
    if let Some(substrings) = &a.content_contains_all {
        for s in substrings {
            let escaped = escape_python_string(s);
            let _ = writeln!(out, "    assert \"{escaped}\" in content");
        }
    }

    // content_contains_any
    if let Some(substrings) = &a.content_contains_any {
        let checks: Vec<String> = substrings
            .iter()
            .map(|s| format!("\"{}\" in content", escape_python_string(s)))
            .collect();
        let condition = checks.join(" or ");
        let _ = writeln!(
            out,
            "    assert {condition}, \"expected content to contain at least one of the specified strings\""
        );
    }

    // content_not_contains
    if let Some(substrings) = &a.content_not_contains {
        for s in substrings {
            let escaped = escape_python_string(s);
            let _ = writeln!(out, "    assert \"{escaped}\" not in content");
        }
    }

    // min_content_length
    if let Some(min) = a.min_content_length {
        let _ = writeln!(
            out,
            "    assert len(content) >= {min}, f\"expected content length >= {min}, got {{len(content)}}\""
        );
    }

    // max_content_length
    if let Some(max) = a.max_content_length {
        let _ = writeln!(
            out,
            "    assert len(content) <= {max}, f\"expected content length <= {max}, got {{len(content)}}\""
        );
    }

    // content_starts_with
    if let Some(prefix) = &a.content_starts_with {
        let escaped = escape_python_string(prefix);
        let _ = writeln!(
            out,
            "    assert content.startswith(\"{escaped}\"), f\"expected content to start with: {escaped}\""
        );
    }

    // content_ends_with
    if let Some(suffix) = &a.content_ends_with {
        let escaped = escape_python_string(suffix);
        let _ = writeln!(
            out,
            "    assert content.endswith(\"{escaped}\"), f\"expected content to end with: {escaped}\""
        );
    }
}

/// Convert a category name to a valid Python module name.
fn sanitize_module_name(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() || ch == '_' {
            out.push(ch);
        } else {
            out.push('_');
        }
    }
    out
}

/// Convert a fixture id to a valid Python identifier.
fn sanitize_ident(s: &str) -> String {
    sanitize_module_name(s)
}

/// Convert a camelCase string to snake_case.
fn camel_to_snake(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 4);
    for (i, ch) in s.chars().enumerate() {
        if ch.is_ascii_uppercase() {
            if i > 0 {
                out.push('_');
            }
            out.push(ch.to_ascii_lowercase());
        } else {
            out.push(ch);
        }
    }
    out
}

/// Format a `serde_json::Value` as a Python literal.
fn json_value_to_python(v: &serde_json::Value) -> String {
    match v {
        serde_json::Value::String(s) => {
            let lower = s.to_lowercase();
            format!("\"{}\"", escape_python_string(&lower))
        }
        serde_json::Value::Bool(true) => "True".to_string(),
        serde_json::Value::Bool(false) => "False".to_string(),
        serde_json::Value::Number(n) => n.to_string(),
        serde_json::Value::Null => "None".to_string(),
        other => format!("\"{}\"", escape_python_string(&other.to_string())),
    }
}

/// Escape a string for use inside a Python `"..."` string literal.
fn escape_python_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}
