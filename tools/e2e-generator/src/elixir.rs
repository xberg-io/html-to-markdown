//! Elixir test generator for html-to-markdown e2e tests.
//!
//! Generates `e2e/elixir/test/{category}_test.exs` using ExUnit.

use crate::fixtures::Fixture;
use anyhow::Result;
use camino::Utf8Path;
use itertools::Itertools;
use std::collections::HashMap;
use std::fmt::Write as FmtWrite;

/// Generate Elixir e2e tests from fixtures into `output_dir/elixir/`.
pub fn generate(fixtures: &[Fixture], output_dir: &Utf8Path) -> Result<usize> {
    let elixir_dir = output_dir.join("elixir");
    let test_dir = elixir_dir.join("test");
    std::fs::create_dir_all(&test_dir)?;

    // Group fixtures by category.
    let by_category: HashMap<&str, Vec<&Fixture>> = fixtures
        .iter()
        .filter(|f| !f.is_skipped_for("elixir"))
        .into_group_map_by(|f| f.resolved_category());

    let mut total = 0;

    for (category, category_fixtures) in &by_category {
        let file_name = format!("{}_test.exs", sanitize_module_name(category));
        let path = test_dir.join(&file_name);
        let content = render_test_file(category, category_fixtures);
        std::fs::write(&path, content)?;
        total += category_fixtures.len();
    }

    // Generate mix.exs.
    let mix_exs = render_mix_exs();
    std::fs::write(elixir_dir.join("mix.exs"), mix_exs)?;

    // Generate test/test_helper.exs (required by ExUnit).
    std::fs::write(test_dir.join("test_helper.exs"), "ExUnit.start()\n")?;

    Ok(total)
}

fn render_mix_exs() -> String {
    r#"defmodule HtmlToMarkdownE2e.MixProject do
  use Mix.Project

  def project do
    [
      app: :html_to_markdown_e2e,
      version: "0.1.0",
      elixir: "~> 1.19",
      start_permanent: false,
      deps: deps()
    ]
  end

  def application do
    [extra_applications: [:logger]]
  end

  defp deps do
    [
      {:html_to_markdown, path: "../../packages/elixir"}
    ]
  end
end
"#
    .to_string()
}

fn render_test_file(category: &str, fixtures: &[&Fixture]) -> String {
    let module_name = category_to_module_name(category);

    let mut out = String::new();
    let _ = writeln!(out, "# E2e tests for category: {category}");
    let _ = writeln!(
        out,
        "# Generated by html-to-markdown-e2e-generator — do not edit manually."
    );
    let _ = writeln!(out);
    let _ = writeln!(out, "defmodule HtmlToMarkdown.{module_name}Test do");
    let _ = writeln!(out, "  use ExUnit.Case");
    let _ = writeln!(out);

    for fixture in fixtures {
        render_test_function(&mut out, fixture);
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "end");

    out
}

fn render_test_function(out: &mut String, fixture: &Fixture) {
    let test_name = &fixture.id;
    let description = &fixture.description;

    let _ = writeln!(out, "  test \"{test_name}: {description}\" do");

    // HTML input.
    let html = match &fixture.html {
        Some(html) => html.as_str(),
        None => {
            let _ = writeln!(out, "    # SKIP: no inline html");
            let _ = writeln!(out, "  end");
            return;
        }
    };

    let escaped_html = escape_elixir_string(html);
    let _ = writeln!(out, "    html = \"{escaped_html}\"");

    // Build options map if specified.
    if let Some(opts) = &fixture.options {
        let entries: Vec<String> = opts
            .iter()
            .map(|(k, v)| {
                let snake_key = camel_to_snake(k);
                let elixir_val = json_value_to_elixir(v);
                format!("\"{snake_key}\" => {elixir_val}")
            })
            .collect();
        let map_literal = entries.join(", ");
        let _ = writeln!(out, "    opts = %{{{map_literal}}}");
    }

    let convert_call = if fixture.options.is_some() {
        "HtmlToMarkdown.convert(html, opts)"
    } else {
        "HtmlToMarkdown.convert(html)"
    };

    // Conversion call + error handling.
    if fixture.assertions.expect_error == Some(true) {
        let _ = writeln!(out, "    {{:error, reason}} = {convert_call}");
        if let Some(contains) = &fixture.assertions.error_contains {
            let escaped = escape_elixir_string(contains);
            let _ = writeln!(out, "    assert to_string(reason) =~ \"{escaped}\"");
        }
        let _ = writeln!(out, "  end");
        return;
    }

    let _ = writeln!(out, "    {{:ok, result}} = {convert_call}");
    let _ = writeln!(out, "    content = result[:content] || \"\"");
    let _ = writeln!(out);

    let a = &fixture.assertions;

    // content_equals
    if let Some(expected) = &a.content_equals {
        let escaped = escape_elixir_string(expected);
        let _ = writeln!(out, "    assert String.trim(content) == \"{escaped}\"");
    }

    // content_not_empty
    if a.content_not_empty == Some(true) {
        let _ = writeln!(out, "    assert String.trim(content) != \"\"");
    }

    // content_is_none
    if a.content_is_none == Some(true) {
        let _ = writeln!(out, "    assert String.trim(content) == \"\"");
    }

    // content_contains_all
    if let Some(substrings) = &a.content_contains_all {
        for s in substrings {
            let escaped = escape_elixir_string(s);
            let _ = writeln!(out, "    assert String.contains?(content, \"{escaped}\")");
        }
    }

    // content_contains_any
    if let Some(substrings) = &a.content_contains_any {
        let checks: Vec<String> = substrings
            .iter()
            .map(|s| format!("String.contains?(content, \"{}\")", escape_elixir_string(s)))
            .collect();
        let condition = checks.join(" or ");
        let _ = writeln!(out, "    assert {condition}");
    }

    // content_not_contains
    if let Some(substrings) = &a.content_not_contains {
        for s in substrings {
            let escaped = escape_elixir_string(s);
            let _ = writeln!(out, "    refute String.contains?(content, \"{escaped}\")");
        }
    }

    // min_content_length
    if let Some(min) = a.min_content_length {
        let _ = writeln!(out, "    assert String.length(content) >= {min}");
    }

    // max_content_length
    if let Some(max) = a.max_content_length {
        let _ = writeln!(out, "    assert String.length(content) <= {max}");
    }

    // content_starts_with
    if let Some(prefix) = &a.content_starts_with {
        let escaped = escape_elixir_string(prefix);
        let _ = writeln!(out, "    assert String.starts_with?(content, \"{escaped}\")");
    }

    // content_ends_with
    if let Some(suffix) = &a.content_ends_with {
        let escaped = escape_elixir_string(suffix);
        let _ = writeln!(out, "    assert String.ends_with?(content, \"{escaped}\")");
    }

    let _ = writeln!(out, "  end");
}

/// Convert a category name to a valid Elixir module name.
fn sanitize_module_name(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() || ch == '_' {
            out.push(ch);
        } else {
            out.push('_');
        }
    }
    out
}

/// Convert a category name to Elixir module naming convention (CamelCase).
fn category_to_module_name(s: &str) -> String {
    let mut out = String::new();
    let mut capitalize_next = true;

    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() {
            if capitalize_next {
                out.push(ch.to_ascii_uppercase());
                capitalize_next = false;
            } else {
                out.push(ch.to_ascii_lowercase());
            }
        } else if ch == '_' || ch == '-' || ch == ' ' {
            capitalize_next = true;
        }
    }

    out
}

/// Convert a camelCase string to snake_case.
fn camel_to_snake(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 4);
    for (i, ch) in s.chars().enumerate() {
        if ch.is_ascii_uppercase() {
            if i > 0 {
                out.push('_');
            }
            out.push(ch.to_ascii_lowercase());
        } else {
            out.push(ch);
        }
    }
    out
}

/// Format a `serde_json::Value` as an Elixir literal.
fn json_value_to_elixir(v: &serde_json::Value) -> String {
    match v {
        serde_json::Value::String(s) => format!("\"{}\"", escape_elixir_string(s)),
        serde_json::Value::Bool(b) => b.to_string(),
        serde_json::Value::Number(n) => n.to_string(),
        serde_json::Value::Null => "nil".to_string(),
        other => format!("\"{}\"", escape_elixir_string(&other.to_string())),
    }
}

/// Escape a string for use inside an Elixir `"..."` string literal.
fn escape_elixir_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
        .replace('#', "\\#")
}
