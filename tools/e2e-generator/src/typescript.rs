//! TypeScript test generator for html-to-markdown e2e tests.
//!
//! Generates `e2e/typescript/tests/{category}.test.ts` and a `package.json`.

use crate::fixtures::Fixture;
use anyhow::Result;
use camino::Utf8Path;
use itertools::Itertools;
use std::collections::HashMap;
use std::fmt::Write as FmtWrite;

/// Generate TypeScript e2e tests from fixtures into `output_dir/typescript/`.
pub fn generate(fixtures: &[Fixture], output_dir: &Utf8Path) -> Result<usize> {
    let ts_dir = output_dir.join("typescript");
    let tests_dir = ts_dir.join("tests");
    std::fs::create_dir_all(&tests_dir)?;

    // Group fixtures by category.
    let by_category: HashMap<&str, Vec<&Fixture>> = fixtures
        .iter()
        .filter(|f| !f.is_skipped_for("typescript"))
        .into_group_map_by(|f| f.resolved_category());

    let mut total = 0;

    for (category, category_fixtures) in &by_category {
        let file_name = format!("{}.test.ts", sanitize_filename(category));
        let path = tests_dir.join(&file_name);
        let content = render_test_file(category, category_fixtures);
        std::fs::write(&path, content)?;
        total += category_fixtures.len();
    }

    // Generate package.json.
    let package_json = render_package_json();
    std::fs::write(ts_dir.join("package.json"), package_json)?;

    Ok(total)
}

fn render_package_json() -> String {
    r#"{
  "name": "html-to-markdown-e2e-typescript",
  "version": "0.1.0",
  "private": true,
  "type": "module",
  "scripts": {
    "test": "vitest run"
  },
  "devDependencies": {
    "@kreuzberg/html-to-markdown": "workspace:*",
    "vitest": "^4.1.2"
  }
}
"#
    .to_string()
}

fn render_test_file(category: &str, fixtures: &[&Fixture]) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "/**");
    let _ = writeln!(out, " * E2e tests for category: {category}");
    let _ = writeln!(
        out,
        " * Generated by html-to-markdown-e2e-generator — do not edit manually."
    );
    let _ = writeln!(out, " */");
    let _ = writeln!(out, "import {{ describe, it, expect }} from 'vitest';");
    let _ = writeln!(out, "import {{ convert }} from '@kreuzberg/html-to-markdown';");
    let _ = writeln!(out);

    let _ = writeln!(out, "describe('{category}', () => {{");

    for fixture in fixtures {
        render_test_function(&mut out, fixture);
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "}});");

    // Ensure final newline
    if !out.ends_with('\n') {
        out.push('\n');
    }
    out
}

fn render_test_function(out: &mut String, fixture: &Fixture) {
    let fn_name = sanitize_test_name(&fixture.id);
    let description = fixture.description.replace('\'', "\\'");

    // HTML input.
    let html = match &fixture.html {
        Some(html) => html.as_str(),
        None => {
            let _ = writeln!(out, "  it.skip('{fn_name}: {description}', () => {{}});");
            return;
        }
    };

    let escaped_html = escape_template_string(html);

    // Build options literal if specified.
    let options_literal = fixture.options.as_ref().map(|opts| {
        let entries: Vec<String> = opts
            .iter()
            .map(|(k, v)| {
                let mut js_val = json_value_to_js(v);
                if let (serde_json::Value::String(s), true) = (
                    v,
                    matches!(
                        k.as_str(),
                        "headingStyle"
                            | "listIndentType"
                            | "whitespaceMode"
                            | "newlineStyle"
                            | "codeBlockStyle"
                            | "highlightStyle"
                            | "linkStyle"
                            | "outputFormat"
                            | "preset"
                    ),
                ) {
                    let mut pascal_case = s
                        .split('_')
                        .map(|part| {
                            let mut c = part.chars();
                            match c.next() {
                                None => String::new(),
                                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
                            }
                        })
                        .collect::<String>();

                    // Handle special cases for NAPI-RS naming
                    if pascal_case == "Atxclosed" {
                        pascal_case = "AtxClosed".to_string();
                    } else if pascal_case == "Doubleequal" {
                        pascal_case = "DoubleEqual".to_string();
                    }

                    js_val = format!("'{}'", escape_string(&pascal_case));
                }
                format!("{k}: {js_val}")
            })
            .collect();
        format!("{{ {} }}", entries.join(", "))
    });

    let convert_arg = if let Some(ref lit) = options_literal {
        format!("html, {lit}")
    } else {
        "html".to_string()
    };

    // Conversion call + error handling.
    if fixture.assertions.expect_error == Some(true) {
        let _ = writeln!(out, "  it('{fn_name}: {description}', () => {{");
        let _ = writeln!(out, "    const html = `{escaped_html}`;");
        let _ = writeln!(out, "    expect(() => convert({convert_arg})).toThrow();");
        if let Some(contains) = &fixture.assertions.error_contains {
            let escaped = escape_string(contains);
            let _ = writeln!(out, "    try {{");
            let _ = writeln!(out, "      convert({convert_arg});");
            let _ = writeln!(out, "      expect.fail('Expected an error');");
            let _ = writeln!(out, "    }} catch (err) {{");
            let _ = writeln!(out, "      expect((err as Error).message).toContain('{escaped}');");
            let _ = writeln!(out, "    }}");
        }
        let _ = writeln!(out, "  }});");
        return;
    }

    let _ = writeln!(out, "  it('{fn_name}: {description}', () => {{");
    let _ = writeln!(out, "    const html = `{escaped_html}`;");
    let _ = writeln!(out, "    const result = convert({convert_arg});");
    let _ = writeln!(out, "    const content = result.content ?? '';");
    let _ = writeln!(out);

    let a = &fixture.assertions;

    // content_equals
    if let Some(expected) = &a.content_equals {
        let escaped = escape_string(expected);
        let _ = writeln!(out, "    expect(content.trim()).toBe('{escaped}');");
    }

    // content_not_empty
    if a.content_not_empty == Some(true) {
        let _ = writeln!(out, "    expect(content.trim().length).toBeGreaterThan(0);");
    }

    // content_is_none
    if a.content_is_none == Some(true) {
        let _ = writeln!(out, "    expect(content.trim()).toBe('');");
    }

    // content_contains_all
    if let Some(substrings) = &a.content_contains_all {
        for s in substrings {
            let escaped = escape_string(s);
            let _ = writeln!(out, "    expect(content).toContain('{escaped}');");
        }
    }

    // content_contains_any
    if let Some(substrings) = &a.content_contains_any {
        let checks: Vec<String> = substrings
            .iter()
            .map(|s| format!("content.includes('{}')", escape_string(s)))
            .collect();
        let condition = checks.join(" || ");
        let _ = writeln!(out, "    expect({condition}).toBe(true);");
    }

    // content_not_contains
    if let Some(substrings) = &a.content_not_contains {
        for s in substrings {
            let escaped = escape_string(s);
            let _ = writeln!(out, "    expect(content).not.toContain('{escaped}');");
        }
    }

    // min_content_length
    if let Some(min) = a.min_content_length {
        let _ = writeln!(out, "    expect(content.length).toBeGreaterThanOrEqual({min});");
    }

    // max_content_length
    if let Some(max) = a.max_content_length {
        let _ = writeln!(out, "    expect(content.length).toBeLessThanOrEqual({max});");
    }

    // content_starts_with
    if let Some(prefix) = &a.content_starts_with {
        let escaped = escape_string(prefix);
        let _ = writeln!(out, "    expect(content).toMatch(/^{}/);", regex_escape(&escaped));
    }

    // content_ends_with
    if let Some(suffix) = &a.content_ends_with {
        let escaped = escape_string(suffix);
        let _ = writeln!(out, "    expect(content).toMatch(/{}$/);", regex_escape(&escaped));
    }

    // ---- Metadata assertions ----
    let has_metadata_assertion = a.metadata_title.is_some()
        || a.metadata_description.is_some()
        || a.metadata_author.is_some()
        || a.metadata_keywords.is_some()
        || a.metadata_canonical_url.is_some()
        || a.metadata_og_title.is_some()
        || a.metadata_og_description.is_some()
        || a.metadata_og_image.is_some()
        || a.metadata_og_type.is_some()
        || a.metadata_og_url.is_some()
        || a.metadata_og_site_name.is_some()
        || a.metadata_twitter_card.is_some()
        || a.metadata_twitter_title.is_some()
        || a.metadata_twitter_description.is_some()
        || a.metadata_has_links.is_some()
        || a.metadata_link_count_min.is_some()
        || a.metadata_links_include_urls.is_some()
        || a.metadata_links_count_min.is_some()
        || a.metadata_has_headers.is_some()
        || a.metadata_header_count_min.is_some()
        || a.metadata_images_count_min.is_some()
        || a.metadata_images_include_srcs.is_some()
        || a.metadata_headings_count_min.is_some()
        || a.metadata_headings_include.is_some();

    if has_metadata_assertion {
        let _ = writeln!(out);
        let _ = writeln!(out, "    // Metadata assertions");
        let _ = writeln!(out, "    expect(result.metadata).not.toBeNull();");
        let _ = writeln!(out, "    const metadata = JSON.parse(result.metadata!);");

        // Document-level metadata
        if let Some(title) = &a.metadata_title {
            let escaped = escape_string(title);
            let _ = writeln!(out, "    expect(metadata.document.title).toBe('{escaped}');");
        }
        if let Some(description) = &a.metadata_description {
            let escaped = escape_string(description);
            let _ = writeln!(out, "    expect(metadata.document.description).toBe('{escaped}');");
        }
        if let Some(author) = &a.metadata_author {
            let escaped = escape_string(author);
            let _ = writeln!(out, "    expect(metadata.document.author).toBe('{escaped}');");
        }
        if let Some(keywords_str) = &a.metadata_keywords {
            for kw in keywords_str.split(',').map(|s| s.trim()) {
                let escaped = escape_string(kw);
                let _ = writeln!(out, "    expect(metadata.document.keywords).toContain('{escaped}');");
            }
        }
        if let Some(canonical_url) = &a.metadata_canonical_url {
            let escaped = escape_string(canonical_url);
            let _ = writeln!(out, "    expect(metadata.document.canonical_url).toBe('{escaped}');");
        }

        // Open Graph metadata
        if let Some(og_title) = &a.metadata_og_title {
            let escaped = escape_string(og_title);
            let _ = writeln!(
                out,
                "    expect(metadata.document.open_graph['title']).toBe('{escaped}');"
            );
        }
        if let Some(og_description) = &a.metadata_og_description {
            let escaped = escape_string(og_description);
            let _ = writeln!(
                out,
                "    expect(metadata.document.open_graph['description']).toBe('{escaped}');"
            );
        }
        if let Some(og_image) = &a.metadata_og_image {
            let escaped = escape_string(og_image);
            let _ = writeln!(
                out,
                "    expect(metadata.document.open_graph['image']).toBe('{escaped}');"
            );
        }
        if let Some(og_type) = &a.metadata_og_type {
            let escaped = escape_string(og_type);
            let _ = writeln!(
                out,
                "    expect(metadata.document.open_graph['type']).toBe('{escaped}');"
            );
        }
        if let Some(og_url) = &a.metadata_og_url {
            let escaped = escape_string(og_url);
            let _ = writeln!(
                out,
                "    expect(metadata.document.open_graph['url']).toBe('{escaped}');"
            );
        }
        if let Some(og_site_name) = &a.metadata_og_site_name {
            let escaped = escape_string(og_site_name);
            let _ = writeln!(
                out,
                "    expect(metadata.document.open_graph['site_name']).toBe('{escaped}');"
            );
        }

        // Twitter metadata
        if let Some(twitter_card) = &a.metadata_twitter_card {
            let escaped = escape_string(twitter_card);
            let _ = writeln!(
                out,
                "    expect(metadata.document.twitter_card['card']).toBe('{escaped}');"
            );
        }
        if let Some(twitter_title) = &a.metadata_twitter_title {
            let escaped = escape_string(twitter_title);
            let _ = writeln!(
                out,
                "    expect(metadata.document.twitter_card['title']).toBe('{escaped}');"
            );
        }
        if let Some(twitter_description) = &a.metadata_twitter_description {
            let escaped = escape_string(twitter_description);
            let _ = writeln!(
                out,
                "    expect(metadata.document.twitter_card['description']).toBe('{escaped}');"
            );
        }

        // Collection assertions: links
        if a.metadata_has_links == Some(true) {
            let _ = writeln!(out, "    expect(metadata.links.length).toBeGreaterThan(0);");
        }
        if let Some(min) = a.metadata_link_count_min {
            let _ = writeln!(out, "    expect(metadata.links.length).toBeGreaterThanOrEqual({min});");
        }
        if let Some(min) = a.metadata_links_count_min {
            let _ = writeln!(out, "    expect(metadata.links.length).toBeGreaterThanOrEqual({min});");
        }
        if let Some(urls) = &a.metadata_links_include_urls {
            let _ = writeln!(out, "    const linkHrefs = metadata.links.map((l: any) => l.href);");
            for url in urls {
                let escaped = escape_string(url);
                let _ = writeln!(out, "    expect(linkHrefs).toContain('{escaped}');");
            }
        }

        // Collection assertions: headers
        if a.metadata_has_headers == Some(true) {
            let _ = writeln!(out, "    expect(metadata.headers.length).toBeGreaterThan(0);");
        }
        if let Some(min) = a.metadata_header_count_min {
            let _ = writeln!(
                out,
                "    expect(metadata.headers.length).toBeGreaterThanOrEqual({min});"
            );
        }

        // Collection assertions: images
        if let Some(min) = a.metadata_images_count_min {
            let _ = writeln!(out, "    expect(metadata.images.length).toBeGreaterThanOrEqual({min});");
        }
        if let Some(srcs) = &a.metadata_images_include_srcs {
            let _ = writeln!(out, "    const imageSrcs = metadata.images.map((i: any) => i.src);");
            for src in srcs {
                let escaped = escape_string(src);
                let _ = writeln!(out, "    expect(imageSrcs).toContain('{escaped}');");
            }
        }

        // Collection assertions: headings
        if let Some(min) = a.metadata_headings_count_min {
            let _ = writeln!(
                out,
                "    expect(metadata.headers.length).toBeGreaterThanOrEqual({min});"
            );
        }
        if let Some(texts) = &a.metadata_headings_include {
            let _ = writeln!(
                out,
                "    const headingTexts = metadata.headers.map((h: any) => h.text);"
            );
            for text in texts {
                let escaped = escape_string(text);
                let _ = writeln!(out, "    expect(headingTexts).toContain('{escaped}');");
            }
        }
    }

    // ---- Table assertions ----
    if let Some(min) = a.table_count_min {
        let _ = writeln!(out);
        let _ = writeln!(out, "    // Table assertions");
        let _ = writeln!(out, "    expect(result.tables.length).toBeGreaterThanOrEqual({min});");
    }
    if let Some(cell_text) = &a.table_contains_cell {
        let escaped = escape_string(cell_text);
        let _ = writeln!(
            out,
            "    expect(result.tables.some((t) => t.grid.cells.some((c) => c.content.includes('{escaped}')))).toBe(true);"
        );
    }

    // ---- Warning assertions ----
    if a.warnings_empty == Some(true) {
        let _ = writeln!(out);
        let _ = writeln!(out, "    // Warning assertions");
        let _ = writeln!(out, "    expect(result.warnings).toHaveLength(0);");
    }

    let _ = writeln!(out, "  }});");
}

/// Convert a category name to a valid filename.
fn sanitize_filename(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        if ch.is_ascii_alphanumeric() || ch == '_' || ch == '-' {
            out.push(ch);
        } else {
            out.push('_');
        }
    }
    out
}

/// Convert a fixture id to a valid test name.
fn sanitize_test_name(s: &str) -> String {
    // Keep alphanumeric and spaces/underscores, replace others with space
    s.chars()
        .map(|ch| {
            if ch.is_ascii_alphanumeric() || ch == '_' {
                ch
            } else {
                ' '
            }
        })
        .collect::<String>()
        .split_whitespace()
        .collect::<Vec<_>>()
        .join("_")
}

/// Escape a string for use inside a JavaScript template literal (`...`).
fn escape_template_string(s: &str) -> String {
    s.replace('\\', "\\\\").replace('`', "\\`").replace("${", "\\${")
}

/// Format a `serde_json::Value` as a JavaScript literal.
fn json_value_to_js(v: &serde_json::Value) -> String {
    match v {
        serde_json::Value::String(s) => format!("'{}'", escape_string(s)),
        serde_json::Value::Bool(b) => b.to_string(),
        serde_json::Value::Number(n) => n.to_string(),
        serde_json::Value::Null => "null".to_string(),
        other => format!("'{}'", escape_string(&other.to_string())),
    }
}

/// Escape a string for use inside a JavaScript string literal.
fn escape_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace("'", "\\'")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}

/// Escape a string for use in a regex pattern.
fn regex_escape(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('.', "\\.")
        .replace('^', "\\^")
        .replace('$', "\\$")
        .replace('*', "\\*")
        .replace('+', "\\+")
        .replace('?', "\\?")
        .replace('(', "\\(")
        .replace(')', "\\)")
        .replace('[', "\\[")
        .replace(']', "\\]")
        .replace('{', "\\{")
        .replace('}', "\\}")
        .replace('|', "\\|")
}
